/**
 * Gera ajustes para TODOS os spots do Brasil
 * Mescla ajustes manuais (prioridade) com automÃ¡ticos (fallback)
 */
export function getAllSpotAdjustments(): SpotWaveAdjustment[] {
  const allAdjustments: SpotWaveAdjustment[] = [];
  
  // Coletar todos os spots de todos os estados
  const allSpots: any[] = [];
  brazilianSurfSpots.forEach(state => {
    if (!state?.cities) return;
    state.cities.forEach(city => {
      if (!city?.beaches) return;
      city.beaches.forEach(beach => {
        if (!beach?.spots) return;
        beach.spots.forEach(spot => {
          allSpots.push(spot);
        });
      });
    });
  });
  
  // Para cada spot, usar ajuste manual OU gerar automÃ¡tico
  allSpots.forEach(spot => {
    const manualAdjustment = spotWaveAdjustments.find(a => a.spotId === spot.id);
    if (manualAdjustment) {
      allAdjustments.push(manualAdjustment);
    } else {
      const autoAdjustment = gerarAjusteAutomatico(
        spot.id,
        spot.name,
        spot.beach,
        spot.city,
        spot.state,
        spot.beachOrientation || 90,
        spot.latitude,
        spot.longitude
      );
      allAdjustments.push(autoAdjustment as SpotWaveAdjustment);
    }
  });
  
  return allAdjustments;
}

