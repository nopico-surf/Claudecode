/**
 * Aplica os ajustes de altura de onda para um spot especÃ­fico
 */
export function applyWaveAdjustments(
  spotId: string,
  waveHeight: number,
  waveDirection: number
): {
  adjustedHeight: number;
  spotName: string;
  shoalingFactor: number;
  directionMultiplier: number;
  directionReason: string;
} {
  const adjustment = getSpotAdjustmentHybrid(spotId);
  
  if (!adjustment) {
    return {
      adjustedHeight: waveHeight,
      spotName: "Unknown",
      shoalingFactor: 1.0,
      directionMultiplier: 1.0,
      directionReason: "Sem ajustes"
    };
  }
  
  // 1. Aplicar shoaling
  let adjustedHeight = waveHeight * adjustment.shoalingFactor;
  
  // 2. Aplicar proteÃ§Ãµes direcionais
  let directionMultiplier = 1.0;
  let directionReason = "Sem proteÃ§Ã£o direcional";
  
  for (const dirAdj of adjustment.directionAdjustments) {
    if (waveDirection >= dirAdj.minDeg && waveDirection <= dirAdj.maxDeg) {
      directionMultiplier = dirAdj.multiplier;
      directionReason = dirAdj.reason;
      break;
    }
  }
  
  adjustedHeight = adjustedHeight * directionMultiplier;
  
  return {
    adjustedHeight,
    spotName: adjustment.spotName,
    shoalingFactor: adjustment.shoalingFactor,
    directionMultiplier,
    directionReason
  };
}

