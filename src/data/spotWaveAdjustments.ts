/**
 * Busca ajuste (manual ou automÃ¡tico) para um spot
 * SUBSTITUI getSpotAdjustment() para usar sistema hÃ­brido
 */
export function getSpotAdjustmentHybrid(spotId: string): SpotWaveAdjustment | undefined {
  // Primeiro tenta manual
  const manual = spotWaveAdjustments.find(adj => adj.spotId === spotId);
  if (manual) return manual;
  
  // SenÃ£o, gera automÃ¡tico
  const spot = findSpotById(spotId);
  if (!spot) return undefined;
  
  return gerarAjusteAutomatico(
    spot.id,
    spot.name,
    spot.beach,
    spot.city,
    spot.state,
    spot.beachOrientation || 90,
    spot.latitude,
    spot.longitude
  ) as SpotWaveAdjustment;
}

/**
 * Helper: Encontra spot por ID
 */
function findSpotById(spotId: string): any {
  for (const state of brazilianSurfSpots) {
    for (const city of state.cities) {
      for (const beach of city.beaches) {
        for (const spot of beach.spots) {
          if (spot.id === spotId) return spot;
        }
      }
    }
  }
  return null;
}

